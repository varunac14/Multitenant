import java.util.ArrayList;
import java.util.Date;

public class easySprints {
	
	private String SprintNo;
	private Date SprintStartDate;
	private String Duration;
	private String ExpTeamVelocity;
	private String SprintStatus;
	private String SprintTotal;
	private String stories ;
	
	public String getSprintNo() {
		return SprintNo;
	}
	public void setSprintNo(String sprintNo) {
		SprintNo = sprintNo;
	}
	public Date getSprintStartDate() {
		return SprintStartDate;
	}
	public void setSprintStartDate(Date sprintStartDate) {
		SprintStartDate = sprintStartDate;
	}
	public String getDuration() {
		return Duration;
	}
	public void setDuration(String duration) {
		Duration = duration;
	}
	public String getExpTeamVelocity() {
		return ExpTeamVelocity;
	}
	public void setExpTeamVelocity(String expTeamVelocity) {
		ExpTeamVelocity = expTeamVelocity;
	}
	public String getSprintStatus() {
		return SprintStatus;
	}
	public void setSprintStatus(String sprintStatus) {
		SprintStatus = sprintStatus;
	}
	public String getSprintTotal() {
		return SprintTotal;
	}
	public void setSprintTotal(String sprintTotal) {
		SprintTotal = sprintTotal;
	}
	public String getStories() {
		return stories;
	}
	public void setStories(String stories) {
		this.stories = stories;
	}
	
}
